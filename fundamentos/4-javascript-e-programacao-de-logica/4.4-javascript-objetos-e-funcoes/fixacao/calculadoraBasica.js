function sum (primNum, segundoNum) {
    return primNum + segundoNum;
}

function subtraction (primNum, segundoNum) {
    return primNum - segundoNum;
}

function multiplication (primNum, segundoNum) {
    return primNum * segundoNum;
}

function division (primNum, segundoNum) {
    return primNum / segundoNum;
}

function module (primNum, segundoNum) {
    return primNum % segundoNum;
}

console.log (sum(10, 3));
console.log (subtraction(10, 3));
console.log (multiplication(10, 3));
console.log (division(10, 3));
console.log (module(10, 3));